    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed

        String OrderId= OrderiD.getText();
        String CustId= CustomerId.getSelectedItem().toString();
        String orderDate=OrderDate.getText();
        ArrayList<OrderDetailModel> orderList= new ArrayList<>();
        
        DefaultTableModel dtm= (DefaultTableModel) jTable1.getModel();
        for(int i=0; i<dtm.getRowCount();i++){
            String itemCode= (String) jTable1.getValueAt(i, 0);
            Integer ordeQty= Integer.parseInt((String) jTable1.getValueAt(i, 2));
            Double UnitPrice= Double.parseDouble((String) jTable1.getValueAt(i, 3)); 
            OrderDetailModel item= new OrderDetailModel(OrderId,itemCode,ordeQty,UnitPrice);
            orderList.add(item);
        }
        
        OrdersModel order = new OrdersModel(OrderId,orderDate,CustId,orderList);
        boolean isAdded;
        try {
            isAdded = OrderController.placeOrder(order);
             if(isAdded){
                JOptionPane.showMessageDialog(this, "Added Success");
            }
        } 
        
        catch (ClassNotFoundException ex) {
            Logger.getLogger(Orderform.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Orderform.class.getName()).log(Level.SEVERE, null, ex);
        }
      
        
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

