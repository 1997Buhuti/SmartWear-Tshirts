    private void ItemNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ItemNameActionPerformed
        try {
            
            String itemName=ItemName.getSelectedItem().toString();
            PreparedStatement pst = DBConnection.getInstance().getConnection().prepareStatement("select * from item where item_description= ?");
            pst.setObject(1,itemName);
            ResultSet rst= pst.executeQuery();
            if(rst.next()){
                
                ItemCode.setText(String.valueOf(rst.getObject("item_code")));
                txtQuantity.setText(String.valueOf(rst.getObject("item_qtyOnHand")));
                txtUnitPrice.setText(String.valueOf(rst.getObject("item_unitPrice")));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Orderform.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Orderform.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ItemNameActionPerformed

